import Foundation



class GeneralMethods{
    class func getRunningFeelingEmoji(_ idx: Int) -> String{
        switch idx {
        case 0:
            return "😎"
        case 1:
            return "😌"
        case 2:
            return "🙁"
        default:
            return "✅"
        }
    }


    
}
